    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher(response.encodeURL("listDep.jsp")).forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int id = Integer.valueOf(request.getParameter("did"));
        String name = request.getParameter("dname");
        Users us = (Users) request.getSession().getAttribute("USER");
        if (us.getRole().equals("EE")) {
            request.getRequestDispatcher("index.jsp").forward(request, response);
        } else {
            if (DepDB.getAllByID(id)== true) {
               request.setAttribute("msq", "ID already exists");
                request.getRequestDispatcher("addnewDep.jsp").forward(request, response);
            }else{
                Department d = new Department();
                d.setDid(id);
                d.setDname(name);
                DepDB.addNew(d);
                response.sendRedirect("list-department");
            }

        }
    }

